package com.codinghub;

public class Perfsquare {

	public static void main(String[] args)
	{
		int n=11;
         double s=(int)Math.sqrt(n);
        
         if(s*s==n)
         {
        	 System.out.println("perfect square");
        	 
         }
         else
         {        	 
        	 System.out.println("not  a  perfect number");
         }
		
		
		
	}

}
