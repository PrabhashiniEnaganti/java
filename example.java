class example
{
	public static void main(String args[])
	{
		int n=-8;
		if(n>0)
		{
			if(n%2==0)
			{
				System.out.println("even number");
			}
			else
			{
				System.out.println("odd number");
			}
		}
		else
		{
			if(n<0)
			{
				System.out.println("Negative number");
			}
			else
			{
				System.out.println("Zero");
			}
		}
	}
}